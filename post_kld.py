# Объединяем все города в один словарь
CITIES_FOR_RATING: dict[str, tuple[float, float]] = {}
CITIES_FOR_RATING.update(CITIES_MARINE)
CITIES_FOR_RATING.update(CITIES_INLAND)

async def main() -> None:
    # Собираем сообщение (WMO = True, чтобы добавить пояснение «ясно/пасмурно»)
    text = build_msg_common(
        location_name="Калининградская область",
        sea_label="Балтийское море",
        cities_for_rating=CITIES_FOR_RATING,
        use_wmo=True
    )
    # Отправляем его
    await send_message_common(text)

if __name__ == "__main__":
    asyncio.run(main())