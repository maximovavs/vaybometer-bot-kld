"""
post_klg.py  •  Специфичный запуск для Калининградской области.

Здесь мы просто импортируем общую логику из post_common.py и задаём параметры:
  - location_name = "Калининградская область"
  - sea_label     = "Балтийское море"
  - cities_for_rating = словарь из морских и «сухопутных» городов
  - use_wmo = True  (вывод WMO-подсказок)
"""
