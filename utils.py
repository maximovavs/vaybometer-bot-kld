def aqi_color(aqi: int | float | str) -> str:
    """
    Эмодзи уровня AQI:
      ≤ 50   → 🟢
      ≤100   → 🟡
      ≤150   → 🟠
      ≤200   → 🔴
      ≤300   → 🟣
      >300   → 🟤
      «—»/«н/д» → ⚪
    """
    if aqi in ("—", "н/д"):
        return "⚪"
    try:
        val = float(aqi)
    except (TypeError, ValueError):
        return "⚪"
    if val <= 50:
        return "🟢"
    if val <= 100:
        return "🟡"
    if val <= 150:
        return "🟠"
    if val <= 200:
        return "🔴"
    if val <= 300:
        return "🟣"
    return "🟤"

def pm_color(pm: Optional[float | int | str], with_unit: bool = False) -> str:
    """
    Цветовая индикация концентрации PM₂.₅ / PM₁₀:
      ≤ 12   → 🟢
      ≤ 35   → 🟡
      ≤ 55   → 🟠
      ≤150   → 🔴
      ≤250   → 🟣
      >250   → 🟤
      None / «—»/«н/д» → ⚪ н/д

    Если with_unit=True, добавляет « µg/м³».
    """
    if pm in (None, "—", "н/д"):
        return "⚪ н/д"
    try:
        val = float(pm)
    except (TypeError, ValueError):
        return "⚪ н/д"
    if val <= 12:
        emoji = "🟢"
    elif val <= 35:
        emoji = "🟡"
    elif val <= 55:
        emoji = "🟠"
    elif val <= 150:
        emoji = "🔴"
    elif val <= 250:
        emoji = "🟣"
    else:
        emoji = "🟤"
    txt = str(int(round(val)))
    if with_unit:
        txt += " µg/м³"
    return f"{emoji}{txt}"

# ──────────────────────── Индекс «задымления» по PM ───────────────────────

def smoke_index(pm25: Optional[float | int | str],
                pm10:  Optional[float | int | str]) -> tuple[str, str]:
    """
    Грубая оценка «уровня задымления» на основе PM:
      База — по PM2.5 (µg/m³):
        0–25  → низкое
        25–55 → среднее
        >55   → высокое
      Модификатор: если отношение PM2.5/PM10 > 0.6 (мелкая фракция преобладает),
      повышаем уровень на 1 ступень (но не выше «высокого»).

    Возвращает (emoji, label) — 🟢/🟡/🔴 и «низкое/среднее/высокое».
    Если данных нет — (⚪, "н/д").
    """
    def _to_float(x):
        try:
            return float(x)
        except (TypeError, ValueError):
            return None

    p25 = _to_float(pm25)
    p10 = _to_float(pm10)
    if p25 is None:
        return "⚪", "н/д"

    # базовый уровень по PM2.5
    if p25 <= 25:
        lvl = 0  # низкое
    elif p25 <= 55:
        lvl = 1  # среднее
    else:
        lvl = 2  # высокое

    # модификатор по доле мелкой фракции
    if p10 and p10 > 0 and (p25 / p10) > 0.6:
        lvl = min(lvl + 1, 2)

    if lvl == 0:
        return "🟢", "низкое"
    if lvl == 1:
        return "🟡", "среднее"
    return "🔴", "высокое"

