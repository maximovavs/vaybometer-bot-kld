def compass(deg: float) -> str:
    """
    Возвращает одну из 16 сторон света по углу deg (0–360).
    """
    return COMPASS[int((deg / 22.5) + 0.5) % 16]

def clouds_word(pc: int) -> str:
    """
    Возвращает слово по проценту облачности:
      <25% → «ясно»
      <70% → «переменная»
      иначе → «пасмурно»
    """
    if pc < 25:
        return "ясно"
    if pc < 70:
        return "переменная"
    return "пасмурно"

def wind_phrase(km_h: float) -> str:
    """
    Возвращает фразу по скорости ветра (км/ч):
      < 2 → «штиль»
      < 8 → «слабый»
      <14 → «умеренный»
      иначе → «сильный»
    """
    if km_h < 2:
        return "штиль"
    if km_h < 8:
        return "слабый"
    if km_h < 14:
        return "умеренный"
    return "сильный"

def safe(v: Any, unit: str = "") -> str:
    """
    None / 'None' / '—' → «—»
    Число → форматированная строка с единицей:
      safe(5.237, "°C") → «5.2°C»
      safe("10", "мм") → «10мм»
    """
    if v in (None, "None", "—"):
        return "—"
    return f"{v:.1f}{unit}" if isinstance(v, (int, float)) else f"{v}{unit}"

# ──────────────────────── AQI & PM раскраска ─────────────────────────────
def aqi_color(aqi: int | float | str) -> str:
    """
    Возвращает эмодзи уровня AQI:
      ≤50   → 🟢
      ≤100  → 🟡
      ≤150  → 🟠
      ≤200  → 🔴
      ≤300  → 🟣
      >300  → 🟤
      «—»/«н/д» → ⚪
    """
    if aqi in ("—", "н/д"):
        return "⚪"
    aqi = float(aqi)
    if aqi <=  50:
        return "🟢"
    if aqi <= 100:
        return "🟡"
    if aqi <= 150:
        return "🟠"
    if aqi <= 200:
        return "🔴"
    if aqi <= 300:
        return "🟣"
    return "🟤"

def pm_color(pm: Optional[float | int | str], with_unit: bool = False) -> str:
    """
    Быстрая цветовая индикация концентраций PM₂.₅ / PM₁₀:
      ≤12   → 🟢
      ≤35   → 🟡
      ≤55   → 🟠
      ≤150  → 🔴
      ≤250  → 🟣
      >250  → 🟤
      None/«—»/«н/д» → ⚪ н/д

    Если with_unit=True, к числу добавит « µg/м³».
    """
    if pm in (None, "—", "н/д"):
        return "⚪ н/д"
    try:
        val = float(pm)
    except (TypeError, ValueError):
        return "⚪ н/д"

    if val <= 12:
        col = "🟢"
    elif val <= 35:
        col = "🟡"
    elif val <= 55:
        col = "🟠"
    elif val <= 150:
        col = "🔴"
    elif val <= 250:
        col = "🟣"
    else:
        col = "🟤"

    txt = str(int(round(val)))
    if with_unit:
        txt += " µg/м³"
    return f"{col}{txt}"

