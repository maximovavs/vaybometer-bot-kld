def get_fact(date: pendulum.Date, region: str = "") -> str:
    """
    Возвращает «факт дня» для даты date и региона region.

    Логика:
    1) Если region содержит «калининград» → факт из FACTS_KLGD по дате (MM-DD) или случайный из FACTS_KLGD_RANDOM.
    2) Если region содержит «кипр» → факт из FACTS_CY по дате либо случайный из DEFAULT_FACTS_CY.
    3) Иначе → факт из DEFAULT_FACTS_UNI.
    """
    r = region.lower()

    # 1) Калининград
    if "калининград" in r:
        key = date.format("MM-DD")
        if key in FACTS_KLGD:
            return FACTS_KLGD[key]
        return random.choice(FACTS_KLGD_RANDOM)

    # 2) Кипр
    if "кипр" in r:
        key = date.format("MM-DD")
        if key in FACTS_CY:
            return FACTS_CY[key]
        return random.choice(DEFAULT_FACTS_CY)

    # 3) Универсальный
    idx = date.day % len(DEFAULT_FACTS_UNI)
    return DEFAULT_FACTS_UNI[idx]

